
#include "pair.h"

namespace MemWar
{

/***********************************************************************
 *                               Constructor                           *
 ***********************************************************************/
Pair::Pair()
{
   block1 = NULL;
   block2 = NULL;
}

/***********************************************************************
 *                               Constructor                           *
 ***********************************************************************/
Pair::Pair(Block* b1, Block* b2)
{
   block1 = b1;
   block2 = b2;
}

/***********************************************************************
 *                               Destructor                            *
 ***********************************************************************/
Pair::~Pair()
{
}

/***********************************************************************
 *                                 clear                               *
 ***********************************************************************/
void Pair::clear()
{
   block1 = NULL;
   block2 = NULL;
}

/***********************************************************************
 *                              setBlock1                              *
 ***********************************************************************/
void Pair::setBlock1(Block* b)
{
   block1 = b;
}

/***********************************************************************
 *                              setBlock2                              *
 ***********************************************************************/
void Pair::setBlock2(Block* b)
{
   block2 = b;
}

/***********************************************************************
 *                              getBlock1                              *
 ***********************************************************************/
Block* Pair::getBlock1()
{
   return(block1);
}

/***********************************************************************
 *                              getBlock2                              *
 ***********************************************************************/
Block* Pair::getBlock2()
{
   return(block2);
}



}

